// A dedicated, FROZEN preview track for the Live Recording screenshot (CI/demo).
//
// WHY THIS EXISTS (and why it isn't a SampleData random-walk ride):
// The Ride Library's sample rides are synthesized random walks — fine on the
// stylized thumbnails, but on the recording screen's REAL MapKit map a random
// walk drifts across oceans/buildings (it has no notion of roads). This track is
// REAL road geometry — the Tail of the Dragon (US-129, Deals Gap, NC/TN), 318
// curves — fetched from a routing engine, so it follows an actual road on land.
//
// Stats are COHERENT BY CONSTRUCTION: speed is derived from real curvature (slow
// hairpins, quicker straights) and each timestamp is integrated as distance /
// speed. So distance, elapsed, and speed reconcile with no fudging (Principle
// P4: correct numbers are sacred). It's a ~14 km partial of an in-progress ride.
//
